#  Find the Simple Interest on Rs. 200 for 5 years at 5% per year

# Given Values 
Principle = 200     # The principal amount in Rs.
r = 5               # Rate of interest per year in percentage
t = 5               # Time period in years

# Calculating Simple Interest 
Simple_interest = (Principle * r * t) / 100

# Displaying the result 
print("The Simple Interest on Rs.", Principle, "for", t, "years at", r,"% per years is Rs.", Simple_interest)